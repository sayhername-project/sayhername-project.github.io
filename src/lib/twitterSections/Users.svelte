    <p>
        At the heart of the #SayHerName movement are the thousands of individuals who transformed their 
        personal and collective grief, outrage, and solidarity into digital action, tweeting #SayHerName 
        <em weight="bold">564,925</em> times in 2015. While we do not claim to know each userâ€™s true intentions behind 
        their tweets, the following research approaches their tweets through the lens of perceived meaning: 
        how their words, tone, and actions may have been received and understood by others within the online public sphere. 
    </p>
    <p>
        By manually scraping and coding a sample of 3,000 tweets, four key motivational patterns were extracted that 
        illustrate why users chose to participate: calling for action, sharing information, amplifying stories, 
        or remembering those lives lost. Complementing this, three key behavioral patterns were discovered that 
        explore how users felt about the conversation: outrage against a broken system, unifying others under this 
        movement, or expressing sorrow about the grievances against the Black female community.
