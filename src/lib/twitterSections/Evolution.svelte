    <h2>The Evolution of #SayHerName throughout 2015</h2>
    <p>
        Throughout 2015, #SayHerName, alongside related hashtags such as #YouOkSis and #FastTailedGirls, 
        became a central space for memorialization and a driving force in the fight for justice for Black 
        women and girls who were victims of policy brutality. Thousands of people tweeted daily, using the 
        hashtag to share stories, express solidarity, and amplify calls for accountability. As the AAPF 
        described, the "#SayHerName campaign was born to cut through this disturbing reality and resist the 
        invisibility of Black women, girls, and femmes by telling their story of police violence."
    </p>
    <p>
        Across the United States, the movement inspired protests, vigils, and community gatherings that 
        mourned lives lost and demanded systemic change, capturing national and international audiences who 
        stood in solidarity with its message. During this period, hashtag activism emerged as a powerful form 
